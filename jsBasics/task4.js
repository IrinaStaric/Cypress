
let radius = 2; // Smaller radius for simplicity
let circleArea = Math.PI * radius * radius; 
console.log("Circle area is:", circleArea.toFixed(2));

let length = 5;
let width = 3;
let rectangleArea = length * width;
console.log("Rectangle area is:", rectangleArea.toFixed(2));

let radiusCylinder = 3;
let height = 4;
let cylinderVolume = Math.PI * radiusCylinder * radiusCylinder * height; 
console.log("Cylinder volume is:", cylinderVolume.toFixed(2));
